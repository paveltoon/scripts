function getActualDateTime(date){
        year = date.getFullYear();
        month = date.getMonth()+1;
        dt = date.getDate();
        hours = date.getHours();
        mins = date.getMinutes();
        secs = date.getSeconds();

        if (dt < 10) {
          dt = '0' + dt;
        }
        if (month < 10) {
          month = '0' + month;
        }
        if (mins < 10) {
          mins = '0' + mins;
        }
        if (secs < 10) {
          secs = '0' + secs;
        }

        return(year + '-' + month + '-' + dt + " " + hours + ":" + mins + ":" + secs);
};

